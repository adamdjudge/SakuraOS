static bool shift = false;
static bool ctrl = false;
static bool alt = false;

static uint8_t read_data()
{
    while ((in_byte(PS2_CMD) & PS2_STATUS_OUT) == 0) {}
    return in_byte(PS2_DATA);

    int delay = 10000;
    while (delay > 0)
        delay--;
}

static void write_command(uint8_t cmd)
{
    while (in_byte(PS2_CMD) & PS2_STATUS_IN) {}
    out_byte_wait(PS2_CMD, cmd);

    int delay = 10000;
    while (delay > 0)
        delay--;
}

static void write_data(uint8_t data)
{
    while (in_byte(PS2_CMD) & PS2_STATUS_IN) {}
    out_byte_wait(PS2_DATA, data);

    int delay = 10000;
    while (delay > 0)
        delay--;
}

void ps2_init()
{
    uint8_t config;

    write_command(PS2_CMD_DISABLE1);
    write_command(PS2_CMD_DISABLE2);

    in_byte(PS2_DATA); // Flush out buffer
    
    write_command(PS2_CMD_READ_CONFIG);
    config = read_data() | PS2_CONFIG_PORT1_INT | PS2_CONFIG_PORT2_INT;
    write_command(PS2_CMD_WRITE_CONFIG);
    write_data(config);

    write_command(PS2_CMD_ENABLE1);
    write_command(PS2_CMD_ENABLE2);

    write_command(KEYBOARD_CMD_ENABLE);

    write_command(PS2_CMD_WRITE_PORT2);
    write_data(MOUSE_CMD_DEFAULT);
    read_data(); // Acknowledge

    write_command(PS2_CMD_WRITE_PORT2);
    write_data(MOUSE_CMD_ENABLE);
    read_data(); // Acknowledge
}

void handle_keyboard_irq()
{
    uint8_t scancode = read_data();
    bool release = (scancode & KEY_RELEASE) != 0;
    scancode &= ~KEY_RELEASE;

    if (scancode == KEY_LSHIFT || scancode == KEY_RSHIFT)
        shift = !release;
    else if (scancode == KEY_CTRL)
        ctrl = !release;
    else if (scancode == KEY_ALT)
        alt = !release;
    else if (!release)
        tty_handle_input(0, shift ? shift_keymap[scancode] : keymap[scancode]);
}

